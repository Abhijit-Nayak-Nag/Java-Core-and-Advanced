package First_Program.Operators;

public class Arithmetic {
    public static void main(String[] args) {
        //a and b works as a operands while +, -, *, /, % works as a operators.
        int a =8;
        int b= 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        //"/" this operator print denominator.
        System.out.println(a/b);
        //"%" this operator print reminder only. \n used for new line.
        System.out.println(a%b +"\n");
       // another way to throw your program into next line "System.out.println( " ");"

        double x =2;
        double y =5;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);
    }
}
