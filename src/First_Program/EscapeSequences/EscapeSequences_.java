package First_Program.EscapeSequences;

public class EscapeSequences_ {
    public static void main(String[] args) {
        System.out.println("Hello GL");
        //insert single quote
        System.out.println("Hello 'GL' ");
        //insert double quote
        System.out.println("\"Hello GL\"");
        System.out.println("Hello \"GL\"");
        //insert new line
        System.out.println("Hello \nGL");
        //insert new tab...space
        System.out.println("Hello \tGl");
        //delete one space or backspace
        System.out.println("Hello \bGL");

    }
}
